package Aula01

class OperateTwoInts {
    fun sumTwoInts(a: Int, b: Int): Int {
        return a+b
    }

    fun subTwoInts(a: Int, b: Int): Int {
        return a-b
    }

    fun multiplyTwoInts(a: Int, b: Int): Int {
        return a*b
    }

    fun divideTwoInts(a: Float, b: Float): Float {
        return a/b
    }
}